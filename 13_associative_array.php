<?php declare(strict_types=1);
/*ASSOCIATIVE ARRAYS SYNTAX*/
$age= array('Arko'=>20,'Rick'=>20,'Russev'=>30);
foreach($age as $key=>$value){
    echo "Age of ".$key." is ".$value."<br>";
}
/*IN ASSOCIATIVE ARRAYS WE ACCESS THE ELEMENTS USING KEYS JUST LIKE WE USE INDEX TO ACCESS AN ELEMENT OF INDEXED ARRAY.*/
?>